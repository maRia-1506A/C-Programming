//ASCII to character
#include<stdio.h>
int main() {
    int n;
    printf("Enter any ASCII value:");
    scanf("%d", &n);
    printf("The ASCII character is: %c", n);
    return 0;
}


//char to ASCII
#include<stdio.h>
int main() {
    char ch;
    printf("Enter any charater:");
    scanf("%c", &ch);
    printf("The ASCII value: %d", ch);
    return 0;
}

